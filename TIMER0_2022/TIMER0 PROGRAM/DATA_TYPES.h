#ifndef _DATRA_TYPES_h_
#define _DATRA_TYPES_h_

typedef unsigned char  u8_t;            //unsigned character to 8 bits
typedef signed char    s8_t;            //signed character to 8 bits


typedef unsigned short  int u16_t;      //unsigned short integer to 16 bits
typedef signed  short   int s16_t;      //signed short integer to 16 bits


typedef unsigned long  int u32_t;       //unsigned long integer to 32 bits
typedef signed  long   int s32_t;       //signed long integer to 32 bits


typedef float   f32_t;                  //float to 32 bits
typedef double  d64_t;                  //double to 64 bits


#endif